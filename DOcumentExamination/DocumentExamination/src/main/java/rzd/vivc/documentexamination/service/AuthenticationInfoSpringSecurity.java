package rzd.vivc.documentexamination.service;

import org.springframework.security.core.Authentication;
import org.springframework.security.core.context.SecurityContextHolder;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.stereotype.Component;

/**
 * Получениеинформации об авторизованном пользователя с использованием Spring
 * Security
 *
 * @author VVolgina
 */
@Component
public class AuthenticationInfoSpringSecurity implements AuthenticationInfoService {

    @Override
    public String getLogin() {
        Authentication authentication = SecurityContextHolder.getContext().getAuthentication();
        Object principal = authentication.getPrincipal();
        String username;
        if (principal instanceof UserDetails) {
            username = ((UserDetails) principal).getUsername();
        } else {
            username = principal.toString();
        }
        return username;
    }

}
